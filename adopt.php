                <h3>Bassets are not lazy</h3>
                    <p>Bassets are hunting dogs. They have been bred for endurance. Many will run circles around you! Additionally, puppies are puppies regardless of their breed. Bassets puppies are curious and often into everything. Rarely are they lazy!</p>
                <h3>Stubborn personality</h3>
                    <p>Many people mistake stubbornness for stupidity. UFBH wants to dispel this common misconception. Bassets are very intelligent highly evolved thinkers that can learn basic obedience. However, if you want a dog that will obey your every command, the basset hound is probably NOT for you. When asked to perform a command, bassets often require a little &quot;persuasion&quot;.</p>
                <h3>Need for attention/Barking when left alone</h3>
                    <p>Most basset hounds are very affectionate dogs and firmly insist, despite their size, on being lap dogs. Basset hounds do not like to be separated from their pack for long periods. They will show their displeasure by barking or howling. Moreover, for a short dog, their barks and howls are very LOUD!</p>
                <h3>Sensitivity to heat</h3>
                    <p>It can get very hot in Utah. Because bassets are very low to the ground, they feel the heat (especially on pavement) reflected off the ground more intensely than humans do. Moreover, you should never leave an unattended dog in a car during the summer months. It may be 80 degrees outside, but in the car, it is more like 100! Your dog can die!</p>
                <h3>Shedding</h3>
                    <p>Yes, bassets are shorthaired dogs. Despite this, bassets shed like crazy. During the spring and fall, many bassets have a “molting” period where they shed even more than normal.</p>
                    <img src="images/bassets/bonnie.jpg" border="0" width="170" height="128" />
                <h3>Bassets do get lost</h3>
                    <p>Bassets are notorious for catching a scent, wandering off from their homes but not being able to find their way back. Bassets should always be on a leash when out in public. For families with yards, UFBH requires a fence with a lock.</p>
                <h3>Health Concerns</h3>
                    <p>There are health concerns specific to every breed of dog. Common ailments in basset hounds include ear infections, development of cysts, glaucoma, and disk disease.</p>
